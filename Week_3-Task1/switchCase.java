package Task1;

public class switchCase {
public static void main(String[] args) {
	int find=20;
	switch(find)
	{
	case 10:System.out.println("10");;
	break;
	case 20: System.out.println("20");
	break;
	case 30: System.out.println("30");
	break;
	default:
	{
		System.out.println("the code is successfully");
	}
	}
}
}
